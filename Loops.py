# вывести сумму двух его соседей. Для элементов списка, являющихся крайними, одним из соседей считается элемент, находящий на 
# противоположном конце этого списка. Например, если на вход подаётся список "1 3 5 6 10", то на выход ожидается список 
# "13 6 9 15 7" (без кавычек). Если на вход пришло только одно число, надо вывести его же. Вывод должен содержать одну строку с 
# числами нового списка, разделёнными пробелом.

n = [int(i) for i in input().split()]

if len(n) == 1:
    print(str(n[0]))
else:
    arr = []
    for i in range(1, len(n)-1):
         arr += [n[i-1] + n[i+1]]
    arr.append(n[0] + n[-2])
    arr.insert(0, n[1] + n[-1])
    print(str(arr)[1:-1].replace(',', ''))

# Напишите программу, которая считывает целые числа с консоли по одному числу в строке. Для каждого введённого числа проверить:
# если число меньше 10, то пропускаем это число;
# если число больше 100, то прекращаем считывать числа;
# в остальных случаях вывести это число обратно на консоль в отдельной строке.

n = int(input())
i = 0
while i >= 0:
    if n < 10:
        i += 1
        n = int(input())
        continue
    if n > 100:
        break
    i += 1
    print(n)
    n = int(input())

# Напишите программу, которая считывает с консоли числа (по одному в строке) до тех пор, пока сумма введённых чисел не будет равна 
# 0 и сразу после этого выводит сумму квадратов всех считанных чисел.

n = int(input())

sum = n
square = n ** 2
while sum != 0:
    n = int(input())
    square += n ** 2
    sum += n
print(square)

