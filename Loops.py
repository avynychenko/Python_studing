# Напишите программу, которая считывает список чисел lst из первой строки и число x из второй строки, которая выводит все позиции, 
# на которых встречается число x в переданном списке lst. Позиции нумеруются с нуля, если число x не встречается в списке, вывести 
# строку "Отсутствует" (без кавычек, с большой буквы).

n = [int(i) for i in input().split()]
p = int(input())

result = []
for i in range(len(n)):
    if n[i] == p:
        result += [i]
if result != []:
    print(str(result)[1:-1].replace(",", " "))   # !!! запись print(*result) делает абсолютно тоже самое
elif result == []:
    print('Отсутствует')

